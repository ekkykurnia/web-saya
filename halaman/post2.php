                    <p style="size:30;color:cyan">Indonesia pulang dari India Terbuka Super Series 2016 dengan satu gelar yang lahir dari All Indonesian Final. Satu final dari wakil Indonesia lainnya gagal berbuah gelar juara.
Satu-satunya titel itu disabet Kevin Sanjaya Sukamuljo/Marcus Fernaldi Gideon yang menang 21-17 dan 21-13 atas sesama ganda putra Indonesia, Angga Pratama/Ricky Karanda Suwardi, di Siri Fort Indoor Stadium, Minggu (3/4/2016).
Game pertama duel 'Merah Putih' itu berjalan ketat. Tapi Kevin/Marcus kemudian mendapat angin selepas berimbang 14-14 dan terus menambah angka untuk memenangi game tersebut. Di game kedua, Angga/Ricky juga tak kuasa meredam laju lawannya. Kevin/Marcus pun terus pegang kendali sedari kedudukan 7-7 dan memenangi partai puncak dengan mengalahkan rival senegaranya yang merupakan unggulan tujuh.

Di partai final lain yang dilangsungkan sebelumnya, pasangan ganda campuran Indonesia Riky Widianto/Richi Puspita Dili harus puas jadi runner-up setelah kalah 13-21 dan 16-21 dari unggulan tujuh asal China, Lu Kai/Huang Yaqiong.

Di satu nomor lain yang juga sudah tuntas, ganda putri, Misaki Matsutomo/Ayaka Takahashi yang unggulan tiga menjadi juara setelah menang 21-18 dan 21-18 atas pasangan Jepang lainnya, Naoko Fukuman/Kurumi Yonao.

Masih ada dua partai nomor tunggal, putra dan putri, yang tersisa. Di tunggal putri ada Ratchanok Intanon dari Thailand yang berhadapan dengan Li Xuerui dari China. Di tunggal putra Viktor Axelsen (Denmark) menghadapi Kento Momota (Jepang).<br>
